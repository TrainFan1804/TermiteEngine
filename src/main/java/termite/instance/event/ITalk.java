package termite.instance.event;

/**
 * To create special talk behavior you need to create a class that implement
 * this interface.
 *
 * @author                              o.le
 * @version                             1.0
 * @since                               0.19
 */
public interface ITalk {

    /**
     * Define here your special talk behavior. This method will be executed
     * automatically.
     */
    void talk();
}
