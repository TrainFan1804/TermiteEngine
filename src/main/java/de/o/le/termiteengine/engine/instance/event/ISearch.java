package de.o.le.termiteengine.engine.instance.event;

/**
 * To create special search behavior you need to create a class that implement
 * this interface.
 *
 * @author                              o.le
 * @version                             1.0
 * @since                               0.24
 */
public interface ISearch {

    /**
     * Define here your special search behavior. This method will be executed
     * automatically.
     */
    void search();
}
