package de.o.le.termite.engine.instance.event;

/**
 * To create special use behavior you need to create a class that implement
 * this interface.
 *
 * @author                              o.le
 * @version                             1.0
 * @since                               0.19
 */
public interface IUse {

    	/**
     	 * Define here your special use behavior. This method will be executed
     	 * automatically.
     	 */
    	void use();
}
